package cl.mineduc.came.apoyo_mejora_continua.modelo.api;

public class Sostenedor {

    private String rutSostenedor;
    private String dvSostenedor;
    private String nombreSostenedor;
    private String paternoSostenedor;
    private String maternoSostenedor;

    public String getRutSostenedor() {
        return rutSostenedor;
    }

    public void setRutSostenedor(String rutSostenedor) {
        this.rutSostenedor = rutSostenedor;
    }

    public String getDvSostenedor() {
        return dvSostenedor;
    }

    public void setDvSostenedor(String dvSostenedor) {
        this.dvSostenedor = dvSostenedor;
    }

    public String getNombreSostenedor() {
        return nombreSostenedor;
    }

    public void setNombreSostenedor(String nombreSostenedor) {
        this.nombreSostenedor = nombreSostenedor;
    }

    public String getPaternoSostenedor() {
        return paternoSostenedor;
    }

    public void setPaternoSostenedor(String paternoSostenedor) {
        this.paternoSostenedor = paternoSostenedor;
    }

    public String getMaternoSostenedor() {
        return maternoSostenedor;
    }

    public void setMaternoSostenedor(String maternoSostenedor) {
        this.maternoSostenedor = maternoSostenedor;
    }

}
