package cl.mineduc.came.apoyo_mejora_continua.modelo;

public class Matricula {
    private Long idMatricula;

    private Long idMatriculaEtnia;

    private Long idNivel;

    private Long idEstablecimiento;

    public Long getIdMatricula() {
        return idMatricula;
    }

    public void setIdMatricula(Long idMatricula) {
        this.idMatricula = idMatricula;
    }

    public Long getIdMatriculaEtnia() {
        return idMatriculaEtnia;
    }

    public void setIdMatriculaEtnia(Long idMatriculaEtnia) {
        this.idMatriculaEtnia = idMatriculaEtnia;
    }

    public Long getIdNivel() {
        return idNivel;
    }

    public void setIdNivel(Long idNivel) {
        this.idNivel = idNivel;
    }

    public Long getIdEstablecimiento() {
        return idEstablecimiento;
    }

    public void setIdEstablecimiento(Long idEstablecimiento) {
        this.idEstablecimiento = idEstablecimiento;
    }
}