public class SupervisorEstablecimiento {
    private Long idSupervisorEstablecimiento;

    private Long idAsignacionTipo;

    private Long idSupervisor;

    private Long idRed;

    private Long idEstablecimiento;

    public Long getIdSupervisorEstablecimiento() {
        return idSupervisorEstablecimiento;
    }

    public void setIdSupervisorEstablecimiento(Long idSupervisorEstablecimiento) {
        this.idSupervisorEstablecimiento = idSupervisorEstablecimiento;
    }

    public Long getIdAsignacionTipo() {
        return idAsignacionTipo;
    }

    public void setIdAsignacionTipo(Long idAsignacionTipo) {
        this.idAsignacionTipo = idAsignacionTipo;
    }

    public Long getIdSupervisor() {
        return idSupervisor;
    }

    public void setIdSupervisor(Long idSupervisor) {
        this.idSupervisor = idSupervisor;
    }

    public Long getIdRed() {
        return idRed;
    }

    public void setIdRed(Long idRed) {
        this.idRed = idRed;
    }

    public Long getIdEstablecimiento() {
        return idEstablecimiento;
    }

    public void setIdEstablecimiento(Long idEstablecimiento) {
        this.idEstablecimiento = idEstablecimiento;
    }
}